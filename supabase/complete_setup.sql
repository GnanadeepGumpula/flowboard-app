-- HELPER: Check if a user has access to a board (SECURITY DEFINER breaks RLS loops)
CREATE OR REPLACE FUNCTION public.has_board_access(p_board_id uuid)
RETURNS boolean
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.boards WHERE id = p_board_id AND created_by = auth.uid()
  ) OR EXISTS (
    SELECT 1 FROM public.board_members WHERE board_id = p_board_id AND user_id = auth.uid() AND status IN ('Accepted', 'Pending')
  );
$$;

-- HELPER: Get a user's role in a board
CREATE OR REPLACE FUNCTION public.get_board_role(p_board_id uuid)
RETURNS text
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT role FROM public.board_members 
  WHERE board_id = p_board_id AND user_id = auth.uid() AND status = 'Accepted' 
  LIMIT 1;
$$;

-- SINGLE UNIFIED NOTIFICATION FUNCTION (Prevents 42725 errors)
CREATE OR REPLACE FUNCTION public.create_notification(
    p_user_id uuid,
    p_title text,
    p_message text,
    p_type text,
    p_related_id uuid DEFAULT NULL,
    p_metadata jsonb DEFAULT '{}'::jsonb
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  INSERT INTO public.notifications (user_id, title, message, type, related_id, metadata)
  VALUES (p_user_id, p_title, p_message, p_type, p_related_id, p_metadata);
END;
$$;

-- HANDLE NEW SIGNUPS
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger 
SECURITY DEFINER SET search_path = public
LANGUAGE plpgsql
AS $$
BEGIN
  INSERT INTO public.profiles (id, email, full_name, avatar_url)
  VALUES (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'name'),
    new.raw_user_meta_data->>'avatar_url'
  );
  RETURN NEW;
END;
$$;

-- TASK AUTOMATIONS & NOTIFICATIONS
CREATE OR REPLACE FUNCTION public.handle_task_changes()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_board_name text;
BEGIN
  IF TG_OP = 'INSERT' THEN
    IF NEW.assigned_to IS NOT NULL AND NEW.status <> 'done' THEN
      IF NEW.assigned_to IS DISTINCT FROM NEW.created_by AND EXISTS (SELECT 1 FROM public.profiles WHERE id = NEW.assigned_to) THEN
        PERFORM public.create_notification(NEW.assigned_to, 'Task assigned', format('You were assigned "%s".', NEW.name), 'task_assigned', NEW.id);
      END IF;
    END IF;
    
    IF NEW.due_date IS NOT NULL THEN
      PERFORM public.send_task_due_reminder_for_task(NEW.id);
    END IF;
    RETURN NEW;
  END IF;

  IF TG_OP = 'UPDATE' THEN
    IF NEW.assigned_to IS NOT NULL AND NEW.status <> 'done' THEN
      IF OLD.assigned_to IS NULL OR NEW.assigned_to IS DISTINCT FROM OLD.assigned_to THEN
        IF NEW.assigned_to IS DISTINCT FROM NEW.created_by AND EXISTS (SELECT 1 FROM public.profiles WHERE id = NEW.assigned_to) THEN
          PERFORM public.create_notification(NEW.assigned_to, 'Task assigned', format('You were assigned "%s".', NEW.name), 'task_assigned', NEW.id);
        END IF;
      END IF;
    END IF;

    IF NEW.status = 'done' AND OLD.status <> 'done' THEN
      SELECT name INTO v_board_name FROM public.boards WHERE id = NEW.board_id;
      IF NEW.created_by IS NOT NULL AND EXISTS (SELECT 1 FROM public.profiles WHERE id = NEW.created_by) THEN
        PERFORM public.create_notification(NEW.created_by, 'Task completed', format('Task "%s" in "%s" was completed.', NEW.name, COALESCE(v_board_name, 'the board')), 'task_completed', NEW.id);
      END IF;
    END IF;

    RETURN NEW;
  END IF;
  RETURN NULL;
END;
$$;

-- BOARD MEMBER INVITATION NOTIFICATIONS (FIXED TO PREVENT OWNER SELF-SPAM)
CREATE OR REPLACE FUNCTION public.notify_board_member_changes()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_board_name text;
  v_owner_id uuid;
  v_requester_name text;
BEGIN
  IF TG_OP = 'INSERT' THEN
    SELECT name, created_by INTO v_board_name, v_owner_id FROM public.boards WHERE id = NEW.board_id;

    -- PREVENT NOTIFICATIONS IF OWNER IS ADDING THEMSELVES
    IF v_owner_id = NEW.user_id THEN
      RETURN NEW;
    END IF;

    -- Check WHO is making the request
    IF auth.uid() = NEW.user_id THEN
      -- SCENARIO A: A user requested access.
      SELECT full_name INTO v_requester_name FROM public.profiles WHERE id = NEW.user_id;
      
      IF v_owner_id IS NOT NULL THEN
        PERFORM public.create_notification(
          v_owner_id,
          'Board Access Request',
          format('%s has requested access to the board "%s".', COALESCE(v_requester_name, 'Someone'), COALESCE(v_board_name, 'a board')),
          'access_request',
          NEW.board_id
        );
      END IF;
      
    ELSE
      -- SCENARIO B: The Owner invited the user.
      IF v_owner_id IS NOT NULL AND v_owner_id IS DISTINCT FROM NEW.user_id THEN
        PERFORM public.create_notification(
          NEW.user_id,
          'Board Invitation',
          format('You have been invited to join the board "%s".', COALESCE(v_board_name, 'a board')),
          'board_invite',
          NEW.board_id
        );
      END IF;
    END IF;
  END IF;
  RETURN NEW;
END;
$$;

-- TASK DUE REMINDERS
CREATE OR REPLACE FUNCTION public.send_task_due_reminder_for_task(p_task_id uuid)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_task_name text;
  v_assigned_to uuid;
  v_due_date timestamp with time zone;
BEGIN
  SELECT name, assigned_to, due_date INTO v_task_name, v_assigned_to, v_due_date
  FROM public.tasks WHERE id = p_task_id;
  
  IF v_assigned_to IS NOT NULL AND v_due_date IS NOT NULL AND v_due_date > now() THEN
    DELETE FROM public.notifications WHERE user_id = v_assigned_to AND related_id = p_task_id AND type = 'task_due';
    PERFORM public.create_notification(
      v_assigned_to,
      'Task Reminder',
      format('Reminder: Your assigned task "%s" is due soon.', v_task_name),
      'task_due',
      p_task_id
    );
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION public.send_task_due_reminder_notification()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  PERFORM public.send_task_due_reminder_for_task(id)
  FROM public.tasks
  WHERE status <> 'done' AND due_date IS NOT NULL AND due_date BETWEEN now() AND (now() + interval '1 day');
END;
$$;

-- AUTOMATIC CREATED_BY ASSIGNMENT
CREATE OR REPLACE FUNCTION public.set_created_by()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  IF NEW.created_by IS NULL THEN
    NEW.created_by := auth.uid();
  END IF;
  RETURN NEW;
END;
$$;

