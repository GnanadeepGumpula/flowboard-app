-- Profiles
CREATE POLICY profiles_read_all ON public.profiles FOR SELECT TO authenticated USING (true);
CREATE POLICY profiles_self_write ON public.profiles FOR UPDATE TO authenticated USING (id = auth.uid()) WITH CHECK (id = auth.uid());

-- Projects
CREATE POLICY projects_owner_read ON public.projects FOR SELECT TO authenticated USING (created_by = auth.uid());
CREATE POLICY projects_owner_write ON public.projects FOR ALL TO authenticated USING (created_by = auth.uid()) WITH CHECK (created_by = auth.uid());

-- Boards
CREATE POLICY boards_read_all ON public.boards FOR SELECT TO authenticated USING (true);
CREATE POLICY boards_owner_write ON public.boards FOR ALL TO authenticated USING (created_by = auth.uid()) WITH CHECK (created_by = auth.uid());

-- Board Members
CREATE POLICY board_members_read ON public.board_members FOR SELECT TO authenticated USING (public.has_board_access(board_id) OR user_id = auth.uid());

CREATE POLICY board_members_insert ON public.board_members FOR INSERT TO authenticated
WITH CHECK (
  EXISTS (SELECT 1 FROM public.boards b WHERE b.id = board_id AND b.created_by = auth.uid())
  OR public.get_board_role(board_id) IN ('Owner', 'Editor', 'Add/Delete Task')
  OR (user_id = auth.uid() AND status = 'Pending')
);

CREATE POLICY board_members_update ON public.board_members FOR UPDATE TO authenticated
USING (
  EXISTS (SELECT 1 FROM public.boards b WHERE b.id = board_id AND b.created_by = auth.uid())
  OR public.get_board_role(board_id) IN ('Owner', 'Editor')
  OR user_id = auth.uid()
);

CREATE POLICY board_members_delete ON public.board_members FOR DELETE TO authenticated
USING (
  EXISTS (SELECT 1 FROM public.boards b WHERE b.id = board_id AND b.created_by = auth.uid())
  OR public.get_board_role(board_id) = 'Owner'
  OR user_id = auth.uid()
);

-- Tasks
CREATE POLICY tasks_board_member_read ON public.tasks FOR SELECT TO authenticated USING (public.has_board_access(board_id));

CREATE POLICY tasks_board_member_update ON public.tasks FOR UPDATE TO authenticated
USING (
  public.get_board_role(board_id) IN ('Update Progress', 'Add/Delete Task', 'Editor')
  OR EXISTS (SELECT 1 FROM public.boards b WHERE b.id = board_id AND b.created_by = auth.uid())
);

CREATE POLICY tasks_board_member_insert ON public.tasks FOR INSERT TO authenticated
WITH CHECK (
  public.get_board_role(board_id) IN ('Add/Delete Task', 'Editor')
  OR EXISTS (SELECT 1 FROM public.boards b WHERE b.id = board_id AND b.created_by = auth.uid())
);

CREATE POLICY tasks_board_member_delete ON public.tasks FOR DELETE TO authenticated
USING (
  public.get_board_role(board_id) IN ('Add/Delete Task', 'Editor')
  OR EXISTS (SELECT 1 FROM public.boards b WHERE b.id = board_id AND b.created_by = auth.uid())
);

-- Notifications
CREATE POLICY notifications_self_read ON public.notifications FOR SELECT TO authenticated USING (user_id = auth.uid());
CREATE POLICY notifications_self_write ON public.notifications FOR ALL TO authenticated USING (user_id = auth.uid()) WITH CHECK (user_id = auth.uid());

